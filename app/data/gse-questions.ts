export const getGseInterpretation = (score: number): string => {
  if (score <= 20) {
    return "خودکارآمدی پایین - شما خودکارآمدی پایینی دارید و نیاز به تقویت آن دارید."
  } else if (score <= 30) {
    return "خودکارآمدی متوسط - شما خودکارآمدی متوسطی دارید که می‌تواند بهبود یابد."
  } else if (score <= 40) {
    return "خودکارآمدی بالا - شما خودکارآمدی خوبی دارید."
  } else {
    return "خودکارآمدی بسیار بالا - شما خودکارآمدی بسیار بالایی دارید."
  }
}

export const getGseSeverity = (score: number): 'پایین' | 'متوسط' | 'بالا' | 'بسیار بالا' => {
  if (score <= 20) return 'پایین'
  if (score <= 30) return 'متوسط'
  if (score <= 40) return 'بالا'
  return 'بسیار بالا'
}

