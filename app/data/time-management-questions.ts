export const getTimeManagementInterpretation = (score: number): string => {
  if (score <= 2) {
    return "مهارت‌های مدیریت زمان شما پایین است. توصیه می‌شود که در دوره‌های مدیریت زمان شرکت کنید."
  } else if (score <= 3) {
    return "مهارت‌های مدیریت زمان شما متوسط است. می‌توانید با تمرین بیشتر بهبود یابید."
  } else if (score <= 4) {
    return "مهارت‌های مدیریت زمان شما خوب است. شما در مدیریت زمان ماهر هستید."
  } else {
    return "مهارت‌های مدیریت زمان شما عالی است. شما در مدیریت زمان بسیار ماهر هستید."
  }
}

export const calculateTimeManagementScore = (answers: string[]): number => {
  const total = answers.reduce((sum, answer) => sum + parseInt(answer), 0)
  return total / answers.length
}

export default timeManagementQuestions


