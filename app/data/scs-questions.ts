export const getScsInterpretation = (score: number): string => {
  if (score <= 65) {
    return "خوددلسوزی پایین - شما خوددلسوزی کمی دارید و نیاز به تقویت آن دارید."
  } else if (score <= 85) {
    return "خوددلسوزی متوسط - شما خوددلسوزی متوسطی دارید که می‌تواند بهبود یابد."
  } else {
    return "خوددلسوزی بالا - شما خوددلسوزی خوبی دارید."
  }
}

export const getScsSeverity = (score: number): 'پایین' | 'متوسط' | 'بالا' => {
  if (score <= 65) return 'پایین'
  if (score <= 85) return 'متوسط'
  return 'بالا'
}

