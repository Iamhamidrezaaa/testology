export const getProblemSolvingInterpretation = (score: number): string => {
  if (score <= 2) {
    return "مهارت‌های حل مسئله شما پایین است. توصیه می‌شود که در دوره‌های حل مسئله شرکت کنید."
  } else if (score <= 3) {
    return "مهارت‌های حل مسئله شما متوسط است. می‌توانید با تمرین بیشتر بهبود یابید."
  } else if (score <= 4) {
    return "مهارت‌های حل مسئله شما خوب است. شما حل‌کننده مسائل ماهری هستید."
  } else {
    return "مهارت‌های حل مسئله شما عالی است. شما حل‌کننده مسائل بسیار ماهری هستید."
  }
}

export const calculateProblemSolvingScore = (answers: string[]): number => {
  const total = answers.reduce((sum, answer) => sum + parseInt(answer), 0)
  return total / answers.length
}

