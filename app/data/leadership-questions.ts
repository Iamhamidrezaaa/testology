export const getLeadershipInterpretation = (score: number): string => {
  if (score <= 2) {
    return "مهارت‌های رهبری شما پایین است. توصیه می‌شود که در دوره‌های رهبری شرکت کنید."
  } else if (score <= 3) {
    return "مهارت‌های رهبری شما متوسط است. می‌توانید با تمرین بیشتر بهبود یابید."
  } else if (score <= 4) {
    return "مهارت‌های رهبری شما خوب است. شما رهبر ماهری هستید."
  } else {
    return "مهارت‌های رهبری شما عالی است. شما رهبر بسیار ماهری هستید."
  }
}

export const calculateLeadershipScore = (answers: string[]): number => {
  const total = answers.reduce((sum, answer) => sum + parseInt(answer), 0)
  return total / answers.length
}

