export const getCreativityInterpretation = (score: number): string => {
  if (score <= 2) {
    return "سطح خلاقیت شما پایین است. توصیه می‌شود که در فعالیت‌های خلاقانه بیشتر شرکت کنید."
  } else if (score <= 3) {
    return "سطح خلاقیت شما متوسط است. می‌توانید با تمرین بیشتر بهبود یابید."
  } else if (score <= 4) {
    return "سطح خلاقیت شما خوب است. شما فردی خلاق و نوآور هستید."
  } else {
    return "سطح خلاقیت شما عالی است. شما یک فرد بسیار خلاق و نوآور هستید."
  }
}

export const calculateCreativityScore = (answers: string[]): number => {
  const total = answers.reduce((sum, answer) => sum + parseInt(answer), 0)
  return total / answers.length
}

