export const getEqInterpretation = (score: number): string => {
  if (score <= 2) {
    return "هوش هیجانی شما پایین است. توصیه می‌شود که در دوره‌های هوش هیجانی شرکت کنید."
  } else if (score <= 3) {
    return "هوش هیجانی شما متوسط است. می‌توانید با تمرین بیشتر بهبود یابید."
  } else if (score <= 4) {
    return "هوش هیجانی شما خوب است. شما فردی با هوش هیجانی بالا هستید."
  } else {
    return "هوش هیجانی شما عالی است. شما فردی با هوش هیجانی بسیار بالا هستید."
  }
}

export const calculateEqScore = (answers: string[]): number => {
  const total = answers.reduce((sum, answer) => sum + parseInt(answer), 0)
  return total / answers.length
}

export default eqQuestions


