export const getTeamworkInterpretation = (score: number): string => {
  if (score <= 2) {
    return "مهارت‌های کار تیمی شما پایین است. توصیه می‌شود که در دوره‌های کار تیمی شرکت کنید."
  } else if (score <= 3) {
    return "مهارت‌های کار تیمی شما متوسط است. می‌توانید با تمرین بیشتر بهبود یابید."
  } else if (score <= 4) {
    return "مهارت‌های کار تیمی شما خوب است. شما عضو تیم ماهری هستید."
  } else {
    return "مهارت‌های کار تیمی شما عالی است. شما عضو تیم بسیار ماهری هستید."
  }
}

export const calculateTeamworkScore = (answers: string[]): number => {
  const total = answers.reduce((sum, answer) => sum + parseInt(answer), 0)
  return total / answers.length
}

