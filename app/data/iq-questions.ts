export const getIqInterpretation = (score: number): string => {
  if (score <= 2) {
    return "هوش شما پایین است. توصیه می‌شود که در دوره‌های آموزشی شرکت کنید."
  } else if (score <= 3) {
    return "هوش شما متوسط است. می‌توانید با تمرین بیشتر بهبود یابید."
  } else if (score <= 4) {
    return "هوش شما خوب است. شما فردی باهوش هستید."
  } else {
    return "هوش شما عالی است. شما فردی بسیار باهوش هستید."
  }
}

export const calculateIqScore = (answers: string[]): number => {
  const total = answers.reduce((sum, answer) => sum + parseInt(answer), 0)
  return total / answers.length
}

