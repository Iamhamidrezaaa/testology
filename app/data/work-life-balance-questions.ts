export const getWorkLifeBalanceInterpretation = (score: number): string => {
  if (score <= 2) {
    return "تعادل کار-زندگی شما پایین است. توصیه می‌شود که در دوره‌های تعادل کار-زندگی شرکت کنید."
  } else if (score <= 3) {
    return "تعادل کار-زندگی شما متوسط است. می‌توانید با تمرین بیشتر بهبود یابید."
  } else if (score <= 4) {
    return "تعادل کار-زندگی شما خوب است. شما در تعادل کار-زندگی ماهر هستید."
  } else {
    return "تعادل کار-زندگی شما عالی است. شما در تعادل کار-زندگی بسیار ماهر هستید."
  }
}

export const calculateWorkLifeBalanceScore = (answers: string[]): number => {
  const total = answers.reduce((sum, answer) => sum + parseInt(answer), 0)
  return total / answers.length
}

