export const getCdRiscInterpretation = (score: number): string => {
  if (score <= 50) {
    return "تاب‌آوری پایین - شما تاب‌آوری کمی دارید."
  } else if (score <= 75) {
    return "تاب‌آوری متوسط - شما تاب‌آوری متوسطی دارید."
  } else if (score <= 100) {
    return "تاب‌آوری بالا - شما تاب‌آوری خوبی دارید."
  } else {
    return "تاب‌آوری بسیار بالا - شما تاب‌آوری بسیار بالایی دارید."
  }
}

export const getCdRiscSeverity = (score: number): 'پایین' | 'متوسط' | 'بالا' | 'بسیار بالا' => {
  if (score <= 50) return 'پایین'
  if (score <= 75) return 'متوسط'
  if (score <= 100) return 'بالا'
  return 'بسیار بالا'
}

