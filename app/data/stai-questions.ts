export const getStaiInterpretation = (score: number): string => {
  if (score <= 2) {
    return "سطح اضطراب شما پایین است. شما فردی آرام و متعادل هستید."
  } else if (score <= 3) {
    return "سطح اضطراب شما متوسط است. ممکن است در برخی موقعیت‌ها احساس اضطراب کنید."
  } else if (score <= 4) {
    return "سطح اضطراب شما بالا است. توصیه می‌شود که با یک متخصص سلامت روان مشورت کنید."
  } else {
    return "سطح اضطراب شما بسیار بالا است. لطفاً فوراً با یک متخصص سلامت روان مشورت کنید."
  }
}

export const calculateStaiScore = (answers: string[]): number => {
  const total = answers.reduce((sum, answer) => sum + parseInt(answer), 0)
  return total / answers.length
}

