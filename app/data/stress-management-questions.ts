export const getStressManagementInterpretation = (score: number): string => {
  if (score <= 2) {
    return "مهارت‌های مدیریت استرس شما پایین است. توصیه می‌شود که در دوره‌های مدیریت استرس شرکت کنید."
  } else if (score <= 3) {
    return "مهارت‌های مدیریت استرس شما متوسط است. می‌توانید با تمرین بیشتر بهبود یابید."
  } else if (score <= 4) {
    return "مهارت‌های مدیریت استرس شما خوب است. شما در مدیریت استرس ماهر هستید."
  } else {
    return "مهارت‌های مدیریت استرس شما عالی است. شما در مدیریت استرس بسیار ماهر هستید."
  }
}

export const calculateStressManagementScore = (answers: string[]): number => {
  const total = answers.reduce((sum, answer) => sum + parseInt(answer), 0)
  return total / answers.length
}

export default stressManagementQuestions


