export const getCommunicationInterpretation = (score: number): string => {
  if (score <= 2) {
    return "مهارت‌های ارتباطی شما نیاز به بهبود دارد. توصیه می‌شود که در دوره‌های ارتباطی شرکت کنید."
  } else if (score <= 3) {
    return "مهارت‌های ارتباطی شما در سطح متوسط است. می‌توانید با تمرین بیشتر بهبود یابید."
  } else if (score <= 4) {
    return "مهارت‌های ارتباطی شما خوب است. شما در اکثر موقعیت‌ها ارتباط موثری برقرار می‌کنید."
  } else {
    return "مهارت‌های ارتباطی شما عالی است. شما یک ارتباط‌گر ماهر هستید."
  }
}

export const calculateCommunicationScore = (answers: string[]): number => {
  const total = answers.reduce((sum, answer) => sum + parseInt(answer), 0)
  return total / answers.length
}

