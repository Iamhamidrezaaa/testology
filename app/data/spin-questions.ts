export const getSpinInterpretation = (score: number): string => {
  if (score <= 17) {
    return "ترس اجتماعی پایین - شما ترس اجتماعی کمی دارید."
  } else if (score <= 34) {
    return "ترس اجتماعی متوسط - شما ترس اجتماعی متوسطی دارید."
  } else if (score <= 51) {
    return "ترس اجتماعی بالا - شما ترس اجتماعی بالایی دارید."
  } else {
    return "ترس اجتماعی بسیار بالا - شما ترس اجتماعی بسیار بالایی دارید که نیاز به مراجعه به متخصص دارد."
  }
}

export const getSpinSeverity = (score: number): 'پایین' | 'متوسط' | 'بالا' | 'بسیار بالا' => {
  if (score <= 17) return 'پایین'
  if (score <= 34) return 'متوسط'
  if (score <= 51) return 'بالا'
  return 'بسیار بالا'
}

