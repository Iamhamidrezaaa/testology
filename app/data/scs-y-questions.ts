export const getScsYInterpretation = (score: number): string => {
  if (score <= 25) {
    return "خوددلسوزی نوجوانان پایین - شما خوددلسوزی نوجوانان کمی دارید."
  } else if (score <= 35) {
    return "خوددلسوزی نوجوانان متوسط - شما خوددلسوزی نوجوانان متوسطی دارید."
  } else {
    return "خوددلسوزی نوجوانان بالا - شما خوددلسوزی نوجوانان خوبی دارید."
  }
}

export const getScsYSeverity = (score: number): 'پایین' | 'متوسط' | 'بالا' => {
  if (score <= 25) return 'پایین'
  if (score <= 35) return 'متوسط'
  return 'بالا'
}

