export const getDersInterpretation = (score: number): string => {
  if (score <= 20) {
    return "تنظیم هیجان پایین - شما تنظیم هیجان کمی دارید."
  } else if (score <= 30) {
    return "تنظیم هیجان متوسط - شما تنظیم هیجان متوسطی دارید."
  } else if (score <= 40) {
    return "تنظیم هیجان بالا - شما تنظیم هیجان بالایی دارید."
  } else {
    return "تنظیم هیجان بسیار بالا - شما تنظیم هیجان بسیار بالایی دارید که نیاز به مراجعه به متخصص دارد."
  }
}

