  // دریافت تست بر اساس ID
  static getTestById(testId: string): TestData | null {
    // ابتدا در تست‌های اصلی جستجو کن
    if (this.testsData[testId]) {
      return this.testsData[testId];
    }
    
    // سپس در تست‌های اضافی جستجو کن
    if (additionalTests[testId as keyof typeof additionalTests]) {
      return additionalTests[testId as keyof typeof additionalTests];
    }
    
    return null;
  }

  // دریافت همه تست‌ها
  static getAllTests(): TestData[] {
    return Object.values(this.testsData);
  }

  // محاسبه نمره تست
  static calculateScore(testId: string, answers: Record<string, number>): number {
    console.log('🔍 calculateScore called with:', { testId, answers });
    
    const test = this.getTestById(testId);
    if (!test) {
      console.error('❌ تست یافت نشد:', testId);
      return 0;
    }

    console.log('✅ تست یافت شد:', test.name);

    const totalQuestions = test.questions.length;
    const totalScore = Object.values(answers).reduce((sum, answer) => sum + answer, 0);
    const maxScore = totalQuestions * 4; // حداکثر نمره (5 گزینه = 4 امتیاز)
    
    console.log('📊 محاسبه نمره:', {
      testId,
      totalQuestions,
      totalScore,
      maxScore,
      answers
    });
    
    const finalScore = Math.round((totalScore / maxScore) * 100);
    console.log('🎯 نمره نهایی:', finalScore);
    
    return finalScore;
  }

