export function getCdRiscInterpretation(answers: Record<string, string>): string {
  const scores = Object.values(answers).map(Number);
  const totalScore = scores.reduce((sum, score) => sum + score, 0);
  const maxScore = 125; // 25 questions * 5 points
  const percentage = (totalScore / maxScore) * 100;

  let interpretation = '';
  if (percentage >= 80) {
    interpretation = 'شما از تاب‌آوری روان‌شناختی بسیار بالایی برخوردار هستید.';
  } else if (percentage >= 60) {
    interpretation = 'شما از تاب‌آوری روان‌شناختی خوبی برخوردار هستید.';
  } else if (percentage >= 40) {
    interpretation = 'شما از تاب‌آوری روان‌شناختی متوسطی برخوردار هستید.';
  } else {
    interpretation = 'شما نیاز به تقویت تاب‌آوری روان‌شناختی خود دارید.';
  }

  return interpretation;
} 